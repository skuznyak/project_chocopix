import { mkdirSync, writeFileSync } from 'node:fs'
import { resolve } from 'node:path'
import { productsCatalog } from '../shared/dist/catalog/products.js'
import { PRERENDER_ROUTES } from './prerender-routes.mjs'

const outputDir = resolve('deploy/nginx')
const outputPath = resolve(outputDir, 'spa-routes.conf')
const legacyRedirects = [{ from: '/cups', to: '/cacao-bombs' }]

const baseRoutes = [
  '/',
  '/404',
  '/cacao-bombs',
  '/marshmallow',
  '/gift-sets',
  '/promotions',
  '/contacts',
  '/privacy-policy',
  '/delivery',
  '/refund',
  '/cart',
  '/checkout',
  '/order-success',
]

const productRoutes = productsCatalog.map((product) => {
  const slug = product.slug ?? product.id
  return `/product/${slug}`
})

const canonicalRoutes = [...new Set([...baseRoutes, ...productRoutes])]
const legacyRedirectRules = legacyRedirects.flatMap(({ from, to }) => [
  `location = ${from} { return 301 ${to}$is_args$args; }`,
  `location = ${from}/ { return 301 ${to}$is_args$args; }`,
])
const redirectFromTrailingSlash = canonicalRoutes
  .filter((route) => route !== '/')
  .map((route) => `location = ${route}/ { return 301 ${route}$is_args$args; }`)

const prerenderedRouteSet = new Set(PRERENDER_ROUTES)
const prerenderedFilePath = (route) => (route === '/' ? '/index.html' : `${route}/index.html`)

const indexRoutes = canonicalRoutes.map((route) => {
  if (prerenderedRouteSet.has(route)) {
    return `location = ${route} { try_files ${prerenderedFilePath(route)} /index.html =404; }`
  }

  return `location = ${route} { try_files /index.html =404; }`
})

const fileContent = [
  '# Generated by scripts/generate-spa-routes.mjs',
  '# Include this file inside `server { ... }` block in nginx config.',
  '# Serves SPA index only for known routes. Unknown routes must return 404.',
  ...legacyRedirectRules,
  ...redirectFromTrailingSlash,
  ...indexRoutes,
  '',
].join('\n')

mkdirSync(outputDir, { recursive: true })
writeFileSync(outputPath, fileContent, 'utf8')

console.log(`Generated nginx SPA routes: ${outputPath} (${canonicalRoutes.length} routes)`)
