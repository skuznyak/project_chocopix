import { mkdirSync, writeFileSync } from 'node:fs'
import { resolve } from 'node:path'
import { productsCatalog } from '../shared/dist/catalog/products.js'

const outputDir = resolve('deploy/nginx')
const outputPath = resolve(outputDir, 'spa-routes.conf')

const baseRoutes = [
  '/',
  '/404',
  '/kakao-bombochky',
  '/gift-sets',
  '/promotions',
  '/contacts',
  '/cart',
  '/checkout',
  '/order-success',
]

const productRoutes = productsCatalog.map((product) => {
  const slug = product.slug ?? product.id
  return `/kakao-bombochka-${slug}`
})

const canonicalRoutes = [...new Set([...baseRoutes, ...productRoutes])]
const redirectFromTrailingSlash = canonicalRoutes
  .filter((route) => route !== '/')
  .map((route) => `location = ${route}/ { return 301 ${route}$is_args$args; }`)

const indexRoutes = canonicalRoutes.map((route) => `location = ${route} { try_files /index.html =404; }`)

const fileContent = [
  '# Generated by scripts/generate-spa-routes.mjs',
  '# Include this file inside `server { ... }` block in nginx config.',
  '# Serves SPA index only for known routes. Unknown routes must return 404.',
  ...redirectFromTrailingSlash,
  ...indexRoutes,
  '',
].join('\n')

mkdirSync(outputDir, { recursive: true })
writeFileSync(outputPath, fileContent, 'utf8')

console.log(`Generated nginx SPA routes: ${outputPath} (${canonicalRoutes.length} routes)`)
